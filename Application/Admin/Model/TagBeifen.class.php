namespace app\index\model;


use think\Model;

class TagBeifen extends Model
{
    public function getId($tag){
        $where['tag']   =   $tag;
        return $this->where($where)->value('id');
    }

    public function getIds($tag){
        $where['tag']   =   array('in',$tag);
        return $this->where($where)->column('id,tag,pid');
    }
}